    private DefaultTableModel montarTabela() {
        String[] colunas = {
            "ID", "Nome", "Status", "Localização", "Data de Devolução", "Responsável", "Motivo da desativação"
        };
        DefaultTableModel tabela = new DefaultTableModel(colunas, 0);

        try {
            FerramentaDAO ferramentaDAO = new FerramentaDAO();
            EmprestimoDAO emprestimoDAO = new EmprestimoDAO();

            List<Ferramenta> ferramentas = ferramentaDAO.listarTodos();

            for (Ferramenta f : ferramentas) {
                Emprestimo emprestimo = emprestimoDAO.buscarEmprestimoAtivoPorFerramenta(f.getId());

                String dataDevolucao = emprestimo != null
                        ? emprestimo.getDataDevolucaoFormatada() : "";

                String responsavel = emprestimo != null ? emprestimo.getResponsavel() : "";

                String motivoDesativacao = "";
                if (f.getStatus().equalsIgnoreCase("Desativada")) {
                    motivoDesativacao = f.getMotivoDesativacao();
                }

                String[] linha = {
                    String.valueOf(f.getId()),
                    f.getNome(),
                    f.getStatus(),
                    f.getLocalizacao(),
                    dataDevolucao,
                    responsavel,
                    motivoDesativacao
                };

                tabela.addRow(linha);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar dados do inventário: " + e.getMessage(),
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }

        return tabela;
    }


