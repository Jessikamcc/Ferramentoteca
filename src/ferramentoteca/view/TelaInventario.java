    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nomeFiltro = txtNome.getText().trim();
        String statusFiltro = jComboBoxStatus.getSelectedItem().toString();
        String localFiltro = jComboBoxLoca.getSelectedItem().toString();

        if (nomeFiltro.isEmpty()
                && statusFiltro.equalsIgnoreCase("Selecione uma opção")
                && localFiltro.equalsIgnoreCase("Selecione uma opção")) {
            JOptionPane.showMessageDialog(this, "Preencha pelo menos um filtro para buscar.",
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DefaultTableModel tabela = (DefaultTableModel) jTable.getModel();
        tabela.setRowCount(0); // limpa a tabela

        try {
            FerramentaDAO ferramentaDAO = new FerramentaDAO();
            EmprestimoDAO emprestimoDAO = new EmprestimoDAO();

            List<Ferramenta> ferramentas = ferramentaDAO.listarTodos();

            for (Ferramenta f : ferramentas) {
                boolean atendeNome = nomeFiltro.isEmpty() || f.getNome().toLowerCase().contains(nomeFiltro.toLowerCase());
                boolean atendeStatus = statusFiltro.equalsIgnoreCase("Selecione uma opção") || f.getStatus().equalsIgnoreCase(statusFiltro);
                boolean atendeLocal = localFiltro.equalsIgnoreCase("Selecione uma opção") || f.getLocalizacao().equalsIgnoreCase(localFiltro);

                if (atendeNome && atendeStatus && atendeLocal) {
                    Emprestimo emprestimo = emprestimoDAO.buscarEmprestimoAtivoPorFerramenta(f.getId());

                    String dataDevolucao = emprestimo != null
                            ? emprestimo.getDataDevolucaoFormatada() : "";

                    String responsavel = emprestimo != null ? emprestimo.getResponsavel() : "";

                    String motivoDesativacao = "";
                    if (f.getStatus().equalsIgnoreCase("Desativada")) {
                        motivoDesativacao = f.getMotivoDesativacao();
                    }

                    String[] linha = {
                        String.valueOf(f.getId()),
                        f.getNome(),
                        f.getStatus(),
                        f.getLocalizacao(),
                        dataDevolucao,
                        responsavel,
                        motivoDesativacao
                    };

                    tabela.addRow(linha);
                }
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao buscar ferramentas: " + e.getMessage(),
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

