    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome = txtIdNomeUsuario.getText().trim();
        String senha = txtIdSenhaUsuario.getText();
        String confirmarSenha = txtConfirmarSenha.getText();
        String tipo = jComboBoxTipo.getSelectedItem().toString();

        if (nome.isEmpty() || senha.isEmpty() || confirmarSenha.isEmpty() || tipo.equals("Selecione")) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos corretamente.",
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!senha.equals(confirmarSenha)) {
            JOptionPane.showMessageDialog(this, "As senhas não coincidem.",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Usuario usuario = new Usuario();
        usuario.setNome(nome);
        usuario.setSenha(senha);  
        usuario.setTipo(tipo);
        usuario.setLimiteEmprestimo(3); // padrão

        try {
            UsuarioDAO dao = new UsuarioDAO();
            dao.inserir(usuario);
            JOptionPane.showMessageDialog(this, "Usuário cadastrado com sucesso!",
                    "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            // Limpar os campos após o cadastro
            txtIdNomeUsuario.setText("");
            txtIdSenhaUsuario.setText("");
            txtConfirmarSenha.setText("");
            jComboBoxTipo.setSelectedIndex(0);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar usuário: " + e.getMessage(),
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAtualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizar1ActionPerformed
        try {
            String nome = txtIdNomeUsuario.getText().trim();
            String senha = new String(txtIdSenhaUsuario.getText());
            String confirmarSenha = new String(txtConfirmarSenha.getText());
            String tipo = (String) jComboBoxTipo.getSelectedItem();

            if (nome.isEmpty() || senha.isEmpty() || confirmarSenha.isEmpty() || tipo.equals("Selecione")) {
                JOptionPane.showMessageDialog(this, "Preencha todos os campos corretamente.");
                return;
            }

            if (!senha.equals(confirmarSenha)) {
                JOptionPane.showMessageDialog(this, "As senhas não coincidem.");
                return;
            }

          
            int id = usuarioAtual.getId(); 

            Usuario usuario = new Usuario();
            usuario.setId(id);
            usuario.setNome(nome);
            usuario.setSenha(senha);
            usuario.setTipo(tipo);

            UsuarioDAO dao = new UsuarioDAO();
            dao.atualizar(usuario);

            JOptionPane.showMessageDialog(this, "Usuário atualizado com sucesso!");
            limparCampos();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar usuário: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAtualizar1ActionPerformed

