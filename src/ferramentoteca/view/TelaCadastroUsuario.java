    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nomeBusca = txtBuscador.getText().trim();

        if (nomeBusca.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite o nome do usuário a ser buscado.");
            return;
        }

        try {
            UsuarioDAO dao = new UsuarioDAO();
            Usuario usuario = dao.buscarPorNome(nomeBusca);

            if (usuario != null) {
                // Preencher os campos da tela
                txtIdNomeUsuario.setText(usuario.getNome());
                txtIdSenhaUsuario.setText(usuario.getSenha());
                txtConfirmarSenha.setText(usuario.getSenha());
                jComboBoxTipo.setSelectedItem(usuario.getTipo());

                // Salva o usuário atual para uso posterior (atualizar/excluir)
                this.usuarioAtual = usuario;

            } else {
                JOptionPane.showMessageDialog(this, "Usuário não encontrado.");
                limparCampos(); // Opcional: limpa a tela se não encontrar
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao buscar usuário: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            int opcao = JOptionPane.showConfirmDialog(this,
                    "Tem certeza que deseja excluir este usuário?", "Confirmar exclusão",
                    JOptionPane.YES_NO_OPTION);

            if (opcao == JOptionPane.YES_OPTION) {
                int id = usuarioAtual.getId(); // Obtido ao buscar o usuário

                UsuarioDAO dao = new UsuarioDAO();
                dao.excluir(id);

                JOptionPane.showMessageDialog(this, "Usuário excluído com sucesso!");
                limparCampos();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir usuário: " + e.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

