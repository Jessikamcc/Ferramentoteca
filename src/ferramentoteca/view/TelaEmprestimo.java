    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if (ferramentaSelecionada == null) {
            JOptionPane.showMessageDialog(this, "Nenhuma ferramenta selecionada!",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String professorNome = txtNomeProfessor.getText().trim();
        String dataEmprestimoStr = txtFieldDataEmprestimo.getText().trim();
        String dataDevolucaoStr = txtFieldDataDevolucao.getText().trim();

        if (professorNome.isEmpty() || dataEmprestimoStr.isEmpty() || dataDevolucaoStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!",
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");  

        try {
            java.util.Date utilDataEmprestimo = sdf.parse(dataEmprestimoStr);
            java.util.Date utilDataDevolucao = sdf.parse(dataDevolucaoStr);

            java.sql.Date dataEmprestimo = new java.sql.Date(utilDataEmprestimo.getTime());
            java.sql.Date dataDevolucao = new java.sql.Date(utilDataDevolucao.getTime());

            Emprestimo emprestimo = new Emprestimo(
                    0, 
                    this.usuarioLogado.getId(), 
                    ferramentaSelecionada.getId(), 
                    professorNome, 
                    utilDataEmprestimo, 
                    utilDataDevolucao, 
                    "Emprestado" 
            );

            
            EmprestimoDAO dao = new EmprestimoDAO();
            dao.registrarEmprestimo(emprestimo);

          
            ferramentaSelecionada.setStatus("Emprestada");
            FerramentaDAO fdao = new FerramentaDAO();
            fdao.atualizar(ferramentaSelecionada);

            JOptionPane.showMessageDialog(this, "Empréstimo registrado com sucesso!");

            limparCampos();

        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Formato de data inválido! Use: yyyy-MM-dd",
                    "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao registrar empréstimo: " + e.getMessage(),
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnConfirmarActionPerformed
    private void limparCampos() {
        txtNomeProfessor.setText("");
        txtFieldDataEmprestimo.setText("");
        txtFieldDataDevolucao.setText("");

        
        ferramentaSelecionada = null;

    }


