    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String criterio = txtIdNome.getText().trim();

        if (criterio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um ID ou nome para buscar",
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            FerramentaDAO dao = new FerramentaDAO();
            Ferramenta ferramenta = dao.buscarParaEmprestimo(criterio);

            if (ferramenta == null) {
                JOptionPane.showMessageDialog(this, "Ferramenta não encontrada",
                        "Aviso", JOptionPane.INFORMATION_MESSAGE);
                limparDadosFerramenta();
                return;
            }

           
            if (ferramenta.getStatus().equalsIgnoreCase("Desativada")) {
                JOptionPane.showMessageDialog(this, "Esta ferramenta se encontra desativada",
                        "Status", JOptionPane.WARNING_MESSAGE);
                limparDadosFerramenta();
                return;
            }

            if (ferramenta.getStatus().equalsIgnoreCase("Emprestada")) {
                JOptionPane.showMessageDialog(this, "Esta ferramenta está emprestada",
                        "Status", JOptionPane.INFORMATION_MESSAGE);
                limparDadosFerramenta();
                return;
            }

          
            mostrarDadosFerramenta(ferramenta);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro na busca: " + e.getMessage(),
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void mostrarDadosFerramenta(Ferramenta ferramenta) {
        jLabelNome.setText("Nome: " + ferramenta.getNome());
        jLabelDescricao.setText("Descrição: " + ferramenta.getDescricao());
        jLabelLocalizacao.setText("Localização: " + ferramenta.getLocalizacao());

        // Habilita o botão de confirmar empréstimo
        btnConfirmar.setEnabled(true);
        ferramentaSelecionada = ferramenta; // Guarda a referência para usar no empréstimo
    }

    private void limparDadosFerramenta() {

        jLabelNome.setText("Nome: ");
        jLabelDescricao.setText("Descrição: ");
        jLabelLocalizacao.setText("Localização: ");

        btnConfirmar.setEnabled(false);
        ferramentaSelecionada = null;

    }//GEN-LAST:event_btnBuscarActionPerformed

